import { randomBytes } from 'node:crypto';
import { chmod, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import process from 'node:process';
import { fileURLToPath, URL } from 'node:url';

const projectRoot = fileURLToPath(new URL('..', import.meta.url));
const envPath = path.join(projectRoot, '.env');
const rotationNotesPath = path.join(projectRoot, 'ROTATEWEEKLY.md');
// Owner read/write only: both files hold plaintext admin credentials.
const CREDENTIAL_FILE_MODE = 0o600;

async function writeCredentialFile(filePath, contents) {
  await writeFile(filePath, contents, {
    encoding: 'utf8',
    mode: CREDENTIAL_FILE_MODE,
  });
  // `mode` above only applies when writeFile creates the file; an existing
  // file (e.g. a pre-existing world-readable .env) keeps its old permissions
  // unless explicitly chmod'd.
  await chmod(filePath, CREDENTIAL_FILE_MODE);
}

async function assertGitignored(relativePath) {
  let gitignore;
  try {
    gitignore = await readFile(path.join(projectRoot, '.gitignore'), 'utf8');
  } catch (error) {
    if (error.code === 'ENOENT') {
      throw new Error(
        `Refusing to write ${relativePath}: no .gitignore was found to confirm it is excluded from version control.`,
        { cause: error },
      );
    }
    throw error;
  }
  if (!gitignore.split('\n').includes(relativePath)) {
    throw new Error(
      `Refusing to write ${relativePath}: it is not listed in .gitignore. Add it before rotating credentials.`,
    );
  }
}

function randomToken(bytes) {
  return randomBytes(bytes).toString('base64url');
}

function newUsername() {
  return `registry-admin-${randomToken(4)}`;
}

function newPassword() {
  // 32 random bytes, base64url-encoded: well above the 12-character
  // ADMIN_PASSWORD minimum enforced in src/config/env.ts.
  return randomToken(32);
}

function setEnvValue(contents, key, value) {
  const pattern = new RegExp(`^${key}=.*$`, 'm');
  const line = `${key}=${value}`;
  if (pattern.test(contents)) {
    return contents.replace(pattern, line);
  }
  const separator = contents.length > 0 && !contents.endsWith('\n') ? '\n' : '';
  return `${contents}${separator}${line}\n`;
}

async function readEnvFile() {
  try {
    return await readFile(envPath, 'utf8');
  } catch (error) {
    if (error.code === 'ENOENT') return '';
    throw error;
  }
}

await assertGitignored('.env');
await assertGitignored('ROTATEWEEKLY.md');

const username = newUsername();
const password = newPassword();

const currentEnv = await readEnvFile();
const updatedEnv = setEnvValue(
  setEnvValue(currentEnv, 'ADMIN_USERNAME', username),
  'ADMIN_PASSWORD',
  password,
);
await writeCredentialFile(envPath, updatedEnv);

const rotatedAt = new Date().toISOString();
const nextRotationDue = new Date(
  Date.now() + 7 * 24 * 60 * 60 * 1000,
).toISOString();

const notes = `# Rotate weekly

Generated by \`pnpm admin:rotate-credentials\`. This file is gitignored and
must never be committed — it holds a plaintext production credential.

- Rotated at: ${rotatedAt}
- Rotate again by: ${nextRotationDue}

## Current local credentials

These have already been written to \`.env\` (ADMIN_USERNAME / ADMIN_PASSWORD)
and take effect the next time the local dev server restarts.

- Username: \`${username}\`
- Password: \`${password}\`

## Apply to production (Vercel)

This script never touches Vercel directly. Run these yourself once you are
ready to roll the change out, or wire them into a cron you control:

\`\`\`sh
vercel env rm ADMIN_USERNAME production --yes
printf '%s' '${username}' | vercel env add ADMIN_USERNAME production

vercel env rm ADMIN_PASSWORD production --yes
printf '%s' '${password}' | vercel env add ADMIN_PASSWORD production

vercel deploy --prod
\`\`\`

After the deploy finishes, verify with a real admin login before discarding
the previous week's credentials from wherever you tracked them.
`;

await writeCredentialFile(rotationNotesPath, notes);

process.stdout.write(`Rotated admin credentials. New username: ${username}\n`);
process.stdout.write('Wrote ADMIN_USERNAME / ADMIN_PASSWORD to .env.\n');
process.stdout.write(
  'Wrote deployment instructions to ROTATEWEEKLY.md (gitignored, local only).\n',
);
